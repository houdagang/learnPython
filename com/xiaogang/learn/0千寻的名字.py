#print() 函数
#单双引号
print('hello python')
# > > hello python
print("hello python")
# > > hello python
print("I don't like stydy at all")
# > > I don't like stydy at all
print('I don\'t like stydy at all')
# > > I don't like stydy at all
print('窗前明月光，\n疑是地上霜')
# > > 窗前明月光，
# > > 疑是地上霜

#三引号
print('''窗前明月光，
疑是地上霜。 
举头望明月，
我叫郭德纲。''')
# > > 窗前明月光，
# > > 疑是地上霜。
# > > 举头望明月，
# > > 我叫郭德纲。
print('''\'张三\'''')

#变量赋值
name = '张三'
print(name)
# > > 张三
age = 18
print(age)
# > > 18


